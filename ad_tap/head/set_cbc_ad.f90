  EXTERNAL VX, VY, VZ
  EXTERNAL VX_AD, VY_AD, VZ_AD
  DOUBLE PRECISION :: val, malfa, mbeta, VX, VY, VZ&
& , dv, ds, vv
  DOUBLE PRECISION :: val_ad, malfa_ad, mbeta_ad, dv_ad, ds_ad, vv_ad
  INTRINSIC MAX
  INTRINSIC ABS
  DOUBLE PRECISION :: x1
  DOUBLE PRECISION :: x1_ad
  DOUBLE PRECISION :: x2
  DOUBLE PRECISION :: x2_ad
  DOUBLE PRECISION :: x3
  DOUBLE PRECISION :: x3_ad
  DOUBLE PRECISION :: x4
  DOUBLE PRECISION :: x4_ad
  DOUBLE PRECISION :: x5
  DOUBLE PRECISION :: x5_ad
  DOUBLE PRECISION :: x6
  DOUBLE PRECISION :: x6_ad
  INTEGER :: arg1
  INTEGER :: branch
! - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
! - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
!  flow due to flow neumann nodes / wellars  - - - - - - - - - - - - - -
  DO ib=first_flow,last_flow
    i = ibc_data(ib, cbc_i)
    j = ibc_data(ib, cbc_j)
    k = ibc_data(ib, cbc_k)
    bcu = ibc_data(ib, cbc_bcu)
    IF (ibc_data(ib, cbc_bctp) .LT. 0) THEN
      tpbcu = 0
    ELSE
      tpbcu = ibc_data(ib, cbc_bctp)
    END IF
    bctype = ibc_data(ib, cbc_bt)
!        "neumann"?, skip otherwise
    IF (bctype .EQ. bt_neum .OR. bctype .EQ. bt_neuw) THEN
!           discrete values
      IF (bcu .LE. 0) THEN
        val = dbc_data(ib, 1, ismpl)
      ELSE
        val = propunit(bcu, idx_hbc, ismpl)
      END IF
      IF (tpbcu .GT. 0 .AND. nbctp .GT. 0) THEN
!               time-dependent bc:  val=ac*val+bc
!               get Alfa and Beta modificators
        CALL GET_TPBCALBE(malfa, mbeta, tpbcu, ismpl)
!               update time dependend modification of the bc-value
        val = malfa + mbeta*val
      END IF
!           wellar test
      IF (val .LT. 0.0d0 .AND. tpbcu .GE. 0) THEN
        CALL PUSHREAL8(ds)
        ds = 0.d0
        dv = 0.d0
        IF (i .GT. 1) THEN
          arg1 = i - 1
          x1 = VX(arg1, j, k, ismpl)
          IF (x1 .GE. 0.) THEN
            CALL PUSHREAL8(vv)
            vv = x1
            CALL PUSHCONTROL1B(0)
          ELSE
            CALL PUSHREAL8(vv)
            vv = -x1
            CALL PUSHCONTROL1B(1)
          END IF
          ds = ds + vv
          dv = dv + conc(i-1, j, k, spec, ismpl)*vv
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        IF (i .LT. i0) THEN
          x2 = VX(i, j, k, ismpl)
          IF (x2 .GE. 0.) THEN
            CALL PUSHREAL8(vv)
            vv = x2
            CALL PUSHCONTROL1B(0)
          ELSE
            CALL PUSHREAL8(vv)
            vv = -x2
            CALL PUSHCONTROL1B(1)
          END IF
          ds = ds + vv
          dv = dv + conc(i+1, j, k, spec, ismpl)*vv
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        IF (j .GT. 1) THEN
          arg1 = j - 1
          x3 = VY(i, arg1, k, ismpl)
          IF (x3 .GE. 0.) THEN
            CALL PUSHREAL8(vv)
            vv = x3
            CALL PUSHCONTROL1B(0)
          ELSE
            CALL PUSHREAL8(vv)
            vv = -x3
            CALL PUSHCONTROL1B(1)
          END IF
          ds = ds + vv
          dv = dv + conc(i, j-1, k, spec, ismpl)*vv
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        IF (j .LT. j0) THEN
          x4 = VY(i, j, k, ismpl)
          IF (x4 .GE. 0.) THEN
            CALL PUSHREAL8(vv)
            vv = x4
            CALL PUSHCONTROL1B(0)
          ELSE
            CALL PUSHREAL8(vv)
            vv = -x4
            CALL PUSHCONTROL1B(1)
          END IF
          ds = ds + vv
          dv = dv + conc(i, j+1, k, spec, ismpl)*vv
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        IF (k .GT. 1) THEN
          arg1 = k - 1
          x5 = VZ(i, j, arg1, ismpl)
          IF (x5 .GE. 0.) THEN
            CALL PUSHREAL8(vv)
            vv = x5
            CALL PUSHCONTROL1B(0)
          ELSE
            CALL PUSHREAL8(vv)
            vv = -x5
            CALL PUSHCONTROL1B(1)
          END IF
          ds = ds + vv
          dv = dv + conc(i, j, k-1, spec, ismpl)*vv
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        IF (k .LT. k0) THEN
          x6 = VZ(i, j, k, ismpl)
          IF (x6 .GE. 0.) THEN
            CALL PUSHREAL8(vv)
            vv = x6
            CALL PUSHCONTROL1B(0)
          ELSE
            CALL PUSHREAL8(vv)
            vv = -x6
            CALL PUSHCONTROL1B(1)
          END IF
          ds = ds + vv
          dv = dv + conc(i, j, k+1, spec, ismpl)*vv
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        CALL PUSHREAL8(dv)
        dv = dv/ds
!              apply dirichlet update [dv]
!              standard boundary condition handling
        CALL PUSHREAL8(conc(i, j, k, spec, ismpl))
        conc(i, j, k, spec, ismpl) = dv
!              mark as boundary for normalising the lin. system
        CALL PUSHCONTROL2B(2)
      ELSE
        CALL PUSHCONTROL2B(1)
      END IF
    ELSE
      CALL PUSHCONTROL2B(0)
    END IF
  END DO
! - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
! dirichlet nodes - - - - - - - - - - - - - - - - - - - - - - - - - - -
  DO ib=first_conc,last_conc
    i = ibc_data(ib, cbc_i)
    j = ibc_data(ib, cbc_j)
    k = ibc_data(ib, cbc_k)
    bcu = ibc_data(ib, cbc_bcu)
    IF (ibc_data(ib, cbc_bctp) .LT. 0) THEN
      tpbcu = 0
    ELSE
      tpbcu = ibc_data(ib, cbc_bctp)
    END IF
    bctype = ibc_data(ib, cbc_bt)
    i_spec = ibc_data(ib, cbc_si)
!        "dirichlet" and "spec"?, skip otherwise
    IF (bctype .EQ. bt_diri .AND. i_spec .EQ. spec) THEN
!           discrete values
      IF (bcu .LE. 0) THEN
        CALL PUSHREAL8(val)
        val = dbc_data(ib, 1, ismpl)
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHREAL8(val)
        val = propunit(bcu, idx_cbc, ismpl)
        CALL PUSHCONTROL1B(1)
      END IF
      IF (tpbcu .GT. 0 .AND. nbctp .GT. 0) THEN
!               time-dependent bc:  val=ac*val+bc
!               get Alfa and Beta modificators
        CALL PUSHINTEGER8(tpbcu)
        CALL PUSHREAL8(mbeta)
        CALL GET_TPBCALBE(malfa, mbeta, tpbcu, ismpl)
!               update time dependend modification of the bc-value
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
      END IF
      IF (tpbcu .GE. 0) THEN
        CALL PUSHCONTROL2B(2)
      ELSE
        CALL PUSHCONTROL2B(1)
      END IF
    ELSE
      CALL PUSHCONTROL2B(0)
    END IF
  END DO
! - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
! neumann  nodes    - - - - - - - - - - - - - - - - - - - - - - - - - -
  DO ib=first_conc,last_conc
    i = ibc_data(ib, cbc_i)
    j = ibc_data(ib, cbc_j)
    k = ibc_data(ib, cbc_k)
    bcu = ibc_data(ib, cbc_bcu)
    IF (ibc_data(ib, cbc_bctp) .LT. 0) THEN
      tpbcu = 0
    ELSE
      tpbcu = ibc_data(ib, cbc_bctp)
    END IF
    bctype = ibc_data(ib, cbc_bt)
    i_spec = ibc_data(ib, cbc_si)
    i_dir = ibc_data(ib, cbc_dir)
!        "neumann" and "spec"?, skip otherwise
    IF (bctype .EQ. bt_neum .AND. i_spec .EQ. spec) THEN
!           discrete values
      IF (bcu .LE. 0) THEN
        CALL PUSHREAL8(val)
        val = dbc_data(ib, 1, ismpl)
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHREAL8(val)
        val = propunit(bcu, idx_cbc, ismpl)
        CALL PUSHCONTROL1B(1)
      END IF
      IF (tpbcu .GT. 0 .AND. nbctp .GT. 0) THEN
!               time-dependent bc:  val=ac*val+bc
!               get Alfa and Beta modificators
        CALL PUSHINTEGER8(tpbcu)
        CALL PUSHREAL8(mbeta)
        CALL GET_TPBCALBE(malfa, mbeta, tpbcu, ismpl)
!               update time dependend modification of the bc-value
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHCONTROL1B(1)
      END IF
      IF (tpbcu .GE. 0) THEN
        IF (i_dir .EQ. 0) THEN
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        IF (i_dir .EQ. 1 .OR. i_dir .EQ. 2) THEN
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        IF (i_dir .EQ. 3 .OR. i_dir .EQ. 4) THEN
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        IF (i_dir .EQ. 5 .OR. i_dir .EQ. 6) THEN
          CALL PUSHCONTROL1B(0)
        ELSE
          CALL PUSHCONTROL1B(1)
        END IF
        CALL PUSHCONTROL2B(2)
      ELSE
        CALL PUSHCONTROL2B(1)
      END IF
    ELSE
      CALL PUSHCONTROL2B(0)
    END IF
  END DO
  DO ib=last_conc,first_conc,-1
    CALL POPCONTROL2B(branch)
    IF (branch .NE. 0) THEN
      IF (branch .EQ. 1) THEN
        val_ad = 0.D0
      ELSE
        i = ibc_data(ib, cbc_i)
        j = ibc_data(ib, cbc_j)
        k = ibc_data(ib, cbc_k)
        val_ad = -w_ad(i, j, k, ismpl)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) val_ad = val_ad/delz(k)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) val_ad = val_ad/dely(j)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) val_ad = val_ad/delx(i)
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) val_ad = val_ad/(delx(i)*dely(j)*delz(k))
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        malfa_ad = val_ad
        mbeta_ad = val*val_ad
        val_ad = mbeta*val_ad
        CALL POPREAL8(mbeta)
        CALL POPINTEGER8(tpbcu)
        CALL GET_TPBCALBE_AD(malfa, malfa_ad, mbeta, mbeta_ad, tpbcu, &
&                      ismpl)
      END IF
      bcu = ibc_data(ib, cbc_bcu)
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPREAL8(val)
        dbc_data_ad(ib, 1, ismpl) = dbc_data_ad(ib, 1, ismpl) + val_ad
      ELSE
        CALL POPREAL8(val)
        propunit_ad(bcu, idx_cbc, ismpl) = propunit_ad(bcu, idx_cbc, &
&         ismpl) + val_ad
      END IF
    END IF
  END DO
  DO ib=last_conc,first_conc,-1
    CALL POPCONTROL2B(branch)
    IF (branch .NE. 0) THEN
      IF (branch .EQ. 1) THEN
        val_ad = 0.D0
      ELSE
        i = ibc_data(ib, cbc_i)
        j = ibc_data(ib, cbc_j)
        k = ibc_data(ib, cbc_k)
        val_ad = conc_ad(i, j, k, spec, ismpl) + w_ad(i, j, k, ismpl)
        conc_ad(i, j, k, spec, ismpl) = 0.D0
        w_ad(i, j, k, ismpl) = 0.D0
        d_ad(i, j, k, ismpl) = 0.D0
        g_ad(i, j, k, ismpl) = 0.D0
        f_ad(i, j, k, ismpl) = 0.D0
        e_ad(i, j, k, ismpl) = 0.D0
        c_ad(i, j, k, ismpl) = 0.D0
        b_ad(i, j, k, ismpl) = 0.D0
        a_ad(i, j, k, ismpl) = 0.D0
      END IF
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        malfa_ad = val_ad
        mbeta_ad = val*val_ad
        val_ad = mbeta*val_ad
        CALL POPREAL8(mbeta)
        CALL POPINTEGER8(tpbcu)
        CALL GET_TPBCALBE_AD(malfa, malfa_ad, mbeta, mbeta_ad, tpbcu, &
&                      ismpl)
      END IF
      bcu = ibc_data(ib, cbc_bcu)
      CALL POPCONTROL1B(branch)
      IF (branch .EQ. 0) THEN
        CALL POPREAL8(val)
        dbc_data_ad(ib, 1, ismpl) = dbc_data_ad(ib, 1, ismpl) + val_ad
      ELSE
        CALL POPREAL8(val)
        propunit_ad(bcu, idx_cbc, ismpl) = propunit_ad(bcu, idx_cbc, &
&         ismpl) + val_ad
      END IF
    END IF
  END DO
  DO ib=last_flow,first_flow,-1
    CALL POPCONTROL2B(branch)
    IF (branch .NE. 0) THEN
      IF (branch .NE. 1) THEN
        i = ibc_data(ib, cbc_i)
        j = ibc_data(ib, cbc_j)
        k = ibc_data(ib, cbc_k)
        CALL POPREAL8(conc(i, j, k, spec, ismpl))
        dv_ad = conc_ad(i, j, k, spec, ismpl) + w_ad(i, j, k, ismpl)
        conc_ad(i, j, k, spec, ismpl) = 0.D0
        w_ad(i, j, k, ismpl) = 0.D0
        d_ad(i, j, k, ismpl) = 0.D0
        g_ad(i, j, k, ismpl) = 0.D0
        f_ad(i, j, k, ismpl) = 0.D0
        e_ad(i, j, k, ismpl) = 0.D0
        c_ad(i, j, k, ismpl) = 0.D0
        b_ad(i, j, k, ismpl) = 0.D0
        a_ad(i, j, k, ismpl) = 0.D0
        CALL POPREAL8(dv)
        ds_ad = -(dv*dv_ad/ds**2)
        dv_ad = dv_ad/ds
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          conc_ad(i, j, k+1, spec, ismpl) = conc_ad(i, j, k+1, spec, &
&           ismpl) + vv*dv_ad
          vv_ad = conc(i, j, k+1, spec, ismpl)*dv_ad + ds_ad
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            CALL POPREAL8(vv)
            x6_ad = vv_ad
          ELSE
            CALL POPREAL8(vv)
            x6_ad = -vv_ad
          END IF
          CALL VZ_AD(i, j, k, ismpl, x6_ad)
        END IF
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          conc_ad(i, j, k-1, spec, ismpl) = conc_ad(i, j, k-1, spec, &
&           ismpl) + vv*dv_ad
          vv_ad = conc(i, j, k-1, spec, ismpl)*dv_ad + ds_ad
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            CALL POPREAL8(vv)
            x5_ad = vv_ad
          ELSE
            CALL POPREAL8(vv)
            x5_ad = -vv_ad
          END IF
          arg1 = k - 1
          CALL VZ_AD(i, j, arg1, ismpl, x5_ad)
        END IF
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          conc_ad(i, j+1, k, spec, ismpl) = conc_ad(i, j+1, k, spec, &
&           ismpl) + vv*dv_ad
          vv_ad = conc(i, j+1, k, spec, ismpl)*dv_ad + ds_ad
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            CALL POPREAL8(vv)
            x4_ad = vv_ad
          ELSE
            CALL POPREAL8(vv)
            x4_ad = -vv_ad
          END IF
          CALL VY_AD(i, j, k, ismpl, x4_ad)
        END IF
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          conc_ad(i, j-1, k, spec, ismpl) = conc_ad(i, j-1, k, spec, &
&           ismpl) + vv*dv_ad
          vv_ad = conc(i, j-1, k, spec, ismpl)*dv_ad + ds_ad
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            CALL POPREAL8(vv)
            x3_ad = vv_ad
          ELSE
            CALL POPREAL8(vv)
            x3_ad = -vv_ad
          END IF
          arg1 = j - 1
          CALL VY_AD(i, arg1, k, ismpl, x3_ad)
        END IF
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          conc_ad(i+1, j, k, spec, ismpl) = conc_ad(i+1, j, k, spec, &
&           ismpl) + vv*dv_ad
          vv_ad = conc(i+1, j, k, spec, ismpl)*dv_ad + ds_ad
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            CALL POPREAL8(vv)
            x2_ad = vv_ad
          ELSE
            CALL POPREAL8(vv)
            x2_ad = -vv_ad
          END IF
          CALL VX_AD(i, j, k, ismpl, x2_ad)
        END IF
        CALL POPCONTROL1B(branch)
        IF (branch .EQ. 0) THEN
          conc_ad(i-1, j, k, spec, ismpl) = conc_ad(i-1, j, k, spec, &
&           ismpl) + vv*dv_ad
          vv_ad = conc(i-1, j, k, spec, ismpl)*dv_ad + ds_ad
          CALL POPCONTROL1B(branch)
          IF (branch .EQ. 0) THEN
            CALL POPREAL8(vv)
            x1_ad = vv_ad
          ELSE
            CALL POPREAL8(vv)
            x1_ad = -vv_ad
          END IF
          arg1 = i - 1
          CALL VX_AD(arg1, j, k, ismpl, x1_ad)
        END IF
        CALL POPREAL8(ds)
      END IF
    END IF
  END DO
END SUBROUTINE SET_CBC_AD

