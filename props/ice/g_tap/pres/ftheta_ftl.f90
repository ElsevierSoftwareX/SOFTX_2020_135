SUBROUTINE g_FTHETA(tlocal, g_tlocal, tliq, wmushy, theta, g_theta&
& , dtheta, g_dtheta, ismpl)
  USE ARRAYS

  USE g_ARRAYS

  USE ICE
  IMPLICIT NONE
  INTEGER :: ismpl
  DOUBLE PRECISION :: tlocal, theta, dtheta, val, tliq, wmushy
  DOUBLE PRECISION :: g_tlocal, g_theta, g_dtheta, g_val
  INTRINSIC EXP
  theta = 1.d0
  dtheta = 0.d0
  IF (tlocal .LT. tliq) THEN
    g_val = g_tlocal/wmushy
    val = (tlocal-tliq)/wmushy
!         Theta=exp((-(T-Tf)/w)^2)
    g_theta = -(EXP(-(val**2))*2*val*g_val)
    theta = EXP(-(val*val))
!         dTheta=-2*(T-Tf)/w^2*Theta;
    g_dtheta = -(2.d0*(theta*g_val+val*g_theta)/wmushy)
    dtheta = -(2.d0*val*theta/wmushy)
